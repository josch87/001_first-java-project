public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Java is cool");

        int a = 12;
        int b = 13;

        int sum = a + b;

        System.out.println("Sum: " + sum);

        int c = 35;
        int d = 25;

        System.out.println(c - d);
        System.out.println(d - c);
        System.out.println(a * b);
        System.out.println(b / a);

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a == b);


        float myFloatA = 12.7f;
        float myFloatB = 27.6f;

        System.out.println(myFloatA - myFloatB);
        System.out.println(myFloatA + myFloatB);
        System.out.println(myFloatA / myFloatB);
        System.out.println(myFloatA * myFloatB);
        System.out.println(myFloatA > myFloatB);
        System.out.println(myFloatA < myFloatB);
        System.out.println(myFloatA == myFloatB);

        double myDoubleA = 721635981762358169325.81768162512591629512d;
        double myDoubleB = 216519785691.81726d;

        System.out.println(myDoubleA - myDoubleB);
        System.out.println(myDoubleA + myDoubleB);
        System.out.println(myDoubleA / myDoubleB);
        System.out.println(myDoubleA * myDoubleB);
        System.out.println(myDoubleA > myDoubleB);
        System.out.println(myDoubleA < myDoubleB);
        System.out.println(myDoubleA == myDoubleB);


    }
}
